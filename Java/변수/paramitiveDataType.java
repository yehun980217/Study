package variables;

public class paramitiveDataType {

    public static void main(String[] args) {
        boolean boo = true; // 논리 자료형
        char ch = 'A'; // 문자
        String str = "안녕하세요"; // 문자열
        int i = 10; // 정수
        long lo = 100000000L; // long 타입 뒤에는 소문자 l 또는 대문자 L 기입, 큰 정수
        double dou = 3.15; // 실수
        float flo = 3.14f; // float 타입 뒤에는 소문자 f 또는 대문자 f 기입, 큰 실수

        System.out.println(boo);
        System.out.println(ch);
        System.out.println(str);
        System.out.println(i);
        System.out.println(lo);
        System.out.println(dou);
        System.out.println(flo);
    }
}
